package question;

public class QuestionDTO {
	
	private String gender;
	private int age;
	private String[] song;
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String[] getSong() {
		return song;
	}
	public void setSong(String[] song) {
		this.song = song;
	}
	
	
	
}
